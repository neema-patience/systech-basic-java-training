// Variables
let x = 10;
let y = 20;

// Equal to
console.log("Equal to (x == y):", x == y); // false

// Strict Equal to (checks type and value)
console.log("Strict Equal to (x === y):", x === y); // false

// Not Equal to
console.log("Not Equal to (x != y):", x != y); // true

// Strict Not Equal to (checks type and value)
console.log("Strict Not Equal to (x !== y):", x !== y); // true

// Greater than
console.log("Greater than (x > y):", x > y); // false

// Less than
console.log("Less than (x < y):", x < y); // true

// Greater than or Equal to
console.log("Greater than or Equal to (x >= y):", x >= y); // false

// Less than or Equal to
console.log("Less than or Equal to (x <= y):", x <= y); // true
