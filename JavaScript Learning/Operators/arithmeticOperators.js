// Variables
let num1, num2;
num1 = 10
num2 = 5;

// Addition ( + )
let addition = num1 + num2; // 10 + 5 = 15
console.log("Addition (num1 + num2):", addition);

// Subtraction ( - )
let subtraction = num1 - num2; // 10 - 5 = 5
console.log("Subtraction (num1 - num2):", subtraction);

// Multiplication ( * )
let multiplication = num1 * num2; // 10 * 5 = 50
console.log("Multiplication (num1 * num2):", multiplication);

// Division ( / )
let division = num1 / num2; // 10 / 5 = 2
console.log("Division (num1 / num2):", division);

// Modulus (remainder) 
let modulus = num1 % num2; // 10 % 5 = 0
console.log("Modulus (num1 % num2):", modulus);

// Exponentiation
let exponentiation = num1 ** 2; // 10 ** 2 = 100
console.log("Exponentiation (num1 ** 2):", exponentiation);
