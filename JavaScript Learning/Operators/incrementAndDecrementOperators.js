// Variables
let count = 80;

// Postfix Increment
console.log("Postfix Increment (count++):", count++); // 5 (count is incremented to 6 after this statement)

console.log("New value of count after postfix increment:", count); // 6


// Prefix Increment
console.log("Prefix Increment (++count):", ++count); // 7 (count is incremented to 7 before this statement)



// Postfix Decrement
console.log("Postfix Decrement (count--):", count--); // 7 (count is decremented to 6 after this statement)

console.log("New value of count after postfix decrement:", count); // 6


// Prefix Decrement
console.log("Prefix Decrement (--count):", --count); // 5 (count is decremented to 5 before this statement)
