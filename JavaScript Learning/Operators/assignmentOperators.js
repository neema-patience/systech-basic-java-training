// Variables

let a, b;
 a = 8;
 b = 4;

// Assignment

a = b; // a is now 4
console.log("Assignment (a = b):", a);


// Addition Assignment
a += b; // Equivalent to a = a + b; a = 4 + 4 = 8

console.log("Addition Assignment (a += b):", a);



// Subtraction Assignment
a -= b; // Equivalent to a = a - b; a = 8 - 4 = 4

console.log("Subtraction Assignment (a -= b):", a);


// Multiplication Assignment
a *= b; // Equivalent to a = a * b; a = 4 * 4 = 16

console.log("Multiplication Assignment (a *= b):", a);


// Division Assignment
a /= b; // Equivalent to a = a / b; a = 16 / 4 = 4

console.log("Division Assignment (a /= b):", a);


// Modulus Assignment
a %= b; // Equivalent to a = a % b; a = 4 % 4 = 0

console.log("Modulus Assignment (a %= b):", a);


// Exponentiation Assignment
a = 2;
a **= 3; // Equivalent to a = a ** 3; a = 2 ** 3 = 8

console.log("Exponentiation Assignment (a **= 3):", a);
